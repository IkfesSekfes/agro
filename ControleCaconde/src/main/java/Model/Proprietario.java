package Model;

public class Proprietario {
    //pk=primary key
    int idProprietarioPK;
    String nomeProprietario, cpfProprietario, matriculaProprietario, carProprietario, ccirProprietario, nirfProprietario;

    public int getIdProprietarioPK() {
        return idProprietarioPK;
    }

    public void setIdProprietarioPK(int idProprietarioPK) {
        this.idProprietarioPK = idProprietarioPK;
    }

    public String getNomeProprietario() {
        return nomeProprietario;
    }

    public void setNomeProprietario(String nomeProprietario) {
        this.nomeProprietario = nomeProprietario;
    }

    public String getCpfProprietario() {
        return cpfProprietario;
    }

    public void setCpfProprietario(String cpfProprietario) {
        this.cpfProprietario = cpfProprietario;
    }

    public String getMatriculaProprietario() {
        return matriculaProprietario;
    }

    public void setMatriculaProprietario(String matriculaProprietario) {
        this.matriculaProprietario = matriculaProprietario;
    }

    public String getCarProprietario() {
        return carProprietario;
    }

    public void setCarProprietario(String carProprietario) {
        this.carProprietario = carProprietario;
    }

    public String getCcirProprietario() {
        return ccirProprietario;
    }

    public void setCcirProprietario(String ccirProprietario) {
        this.ccirProprietario = ccirProprietario;
    }

    public String getNirfProprietario() {
        return nirfProprietario;
    }

    public void setNirfProprietario(String nirfProprietario) {
        this.nirfProprietario = nirfProprietario;
    }
    
    
}
